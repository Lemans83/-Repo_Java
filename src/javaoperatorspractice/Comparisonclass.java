package javaoperatorspractice;

public class Comparisonclass {

    public static void main(String[] args) {
        int i = 10;
        System.out.println(i++); // prints 10
        System.out.println(i++); // print 11

        int b = 20;
        b++;
        System.out.println(b); // prints 21
    }
}
